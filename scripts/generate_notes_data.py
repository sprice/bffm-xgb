def gen_hyperparameter_overrides() -> str:
    """Show manual overrides vs Optuna-tuned values, if any."""
    notes_inputs = load_reference_notes_inputs()
    training_report = _notes_input_dict(notes_inputs, "training_report")
    hp_source = training_report.get("config", {}).get("hyperparameters_source", {})
    overrides = hp_source.get("overrides")
    if not overrides:
        return "*No manual overrides. All hyperparameters as selected by Optuna.*"
    rows = [["Parameter", "Tuned Value", "Used Value"]]
    for param, vals in sorted(overrides.items()):
        rows.append([param, str(vals.get("tuned", "---")), str(vals.get("used", "---"))])
    return pad_table(rows)


def gen_headline_k20() -> str:
    """Key metrics at the K=20 operating point for the abstract."""
    notes_inputs = load_reference_notes_inputs()
    bl = _notes_input_dict(notes_inputs, "baseline_comparison_results")
    comp = _notes_input_dict(notes_inputs, "ml_vs_averaging_comparison")

    k20 = bl["overall"]["20"]
    db = k20["domain_balanced"]
    ca = k20.get("domain_constrained_adaptive", {})
    mi = k20["mini_ipip"]
    at = k20["adaptive_topk"]

    # Find ML-vs-avg deltas at K=20
    db_delta = mi_delta = None
    for e in comp["comparisons"]:
        if e["n_items"] == 20:
            if "domain" in e["method"]:
                db_delta = e
            elif "mini" in e["method"]:
                mi_delta = e

    def ci_str(entry: dict, key: str) -> str:
        ci = entry.get(f"{key}_ci")
        if ci:
            return f" [{fmt_f(ci[0], 4)}, {fmt_f(ci[1], 4)}]"
        return ""

    rows = [
        ["Metric", "Value"],
        [
            "Domain-balanced r",
            f"{fmt_f(db['pearson_r'], 4)}{ci_str(db, 'pearson_r')}",
        ],
        ["Domain-balanced MAE", f"{fmt_f(db['mae'], 2)} pp"],
        ["Domain-balanced 90% coverage", fmt_pct(db["coverage_90"] * 100)],
        [
            "Mini-IPIP r",
            f"{fmt_f(mi['pearson_r'], 4)}{ci_str(mi, 'pearson_r')}",
        ],
        [
            "Constrained-adaptive r",
            f"{fmt_f(ca['pearson_r'], 4)}{ci_str(ca, 'pearson_r')}" if ca else "---",
        ],
        [
            "Adaptive top-K r (greedy)",
            f"{fmt_f(at['pearson_r'], 4)}{ci_str(at, 'pearson_r')}",
        ],
    ]
    if db_delta:
        rows.append(
            [
                "ML vs averaging delta r (domain-balanced)",
                f"{db_delta['delta_r']:+.4f}",
            ]
        )
    if mi_delta:
        rows.append(
            [
                "ML vs averaging delta r (Mini-IPIP)",
                f"{mi_delta['delta_r']:+.4f}",
            ]
        )

    return pad_table(rows)


