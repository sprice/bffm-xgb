def _gen_baselines_from_notes_inputs(notes_inputs: dict) -> str:
    bl = _notes_input_dict(notes_inputs, "baseline_comparison_results")
    overall = bl[
        "overall"
    ]  # nested: overall[str(k)][method] -> {pearson_r, pearson_r_ci, ...}

    strategy_order = [
        ("domain_balanced", "Domain-Balanced"),
        ("domain_constrained_adaptive", "Constrained-Adaptive"),
        ("mini_ipip", "Mini-IPIP"),
        ("first_n", "First-N"),
        ("random", "Random"),
        ("adaptive_topk", "Adaptive Top-K"),
        ("greedy_balanced", "Greedy-Balanced"),
        ("worst_k", "Worst-K"),
    ]
    rows = [["K"] + [label for _, label in strategy_order]]
    for k in [5, 10, 15, 20, 25, 30, 40, 50]:
        k_data = overall.get(str(k), {})
        row = [str(k)]
        for method, _ in strategy_order:
            entry = k_data.get(method)
            if entry is None:
                row.append("---")
                continue
            r = entry["pearson_r"]
            ci = entry.get("pearson_r_ci")
            r_str = fmt_r3(r)
            if ci:
                r_str += f" [{fmt_r3(ci[0])}, {fmt_r3(ci[1])}]"
            if k == 20 and method in ("domain_balanced", "mini_ipip"):
                r_str = f"**{r_str}**"
            row.append(r_str)
        rows.append(row)
    return pad_table(rows)


