def gen_ablation_provenance() -> str:
    """Cross-variant provenance locks for auditability."""
    summary = load_research_summary()
    variants = summary["variants"]

    rows = [
        [
            "Variant",
            "Split Signature",
            "Train SHA256",
            "Hyperparams SHA256",
            "Git Hash",
            "Errors",
        ]
    ]

    for variant in VARIANT_ORDER:
        payload = variants.get(variant, {})
        if not isinstance(payload, dict):
            rows.append([VARIANT_LABELS.get(variant, variant)] + ["---"] * 5)
            continue

        split_sig = _get_nested(payload, ["status", "split_signature"])
        train_sha = _get_nested(payload, ["training", "provenance", "train_sha256"])
        hp_sha = _get_nested(payload, ["training", "provenance", "hyperparameters_sha256"])
        git_hash = _get_nested(payload, ["training", "provenance", "git_hash"])
        errors = payload.get("errors", [])
        err_str = "none"
        if isinstance(errors, list) and errors:
            err_str = f"{len(errors)} issue(s)"

        rows.append(
            [
                VARIANT_LABELS.get(variant, variant),
                (split_sig[:12] if isinstance(split_sig, str) else "---"),
                (train_sha[:12] if isinstance(train_sha, str) else "---"),
                (hp_sha[:12] if isinstance(hp_sha, str) else "---"),
                (git_hash[:12] if isinstance(git_hash, str) else "---"),
                err_str,
            ]
        )

    return pad_table(rows)


def _iter_variant_notes_inputs() -> list[tuple[str, str, dict]]:
    records: list[tuple[str, str, dict]] = []
    for variant in VARIANT_ORDER:
        label = VARIANT_LABELS.get(variant, variant)
        notes_inputs = load_variant_notes_inputs(variant)
        records.append((variant, label, notes_inputs))
    return records


def _gen_sparse20_validation_from_notes_inputs(notes_inputs: dict) -> str:
    val = _notes_input_dict(notes_inputs, "validation_results")
    sparse = val.get("sparse_20", {}).get("metrics", {})
    if not isinstance(sparse, dict) or "overall" not in sparse:
        raise KeyError("validation_results.sparse_20.metrics missing.")
    rows = [["Domain", "r", "MAE", "RMSE", "Within-5", "90% Coverage"]]
    for d in DOMAIN_ORDER:
        dm = sparse[d]
        rows.append(
            [
                DOMAIN_LABELS[d],
                fmt_f(dm["pearson_r"], 4),
                fmt_f(dm["mae"], 2),
                fmt_f(dm["rmse"], 2),
                fmt_pct(dm["within_5_pct"] * 100),
                fmt_pct(dm["coverage_90"] * 100),
            ]
        )
    ov = sparse["overall"]
    rows.append(
        [
            "**Overall**",
            f"**{fmt_f(ov['pearson_r'], 4)}**",
            f"**{fmt_f(ov['mae'], 2)}**",
            f"**{fmt_f(ov['rmse'], 2)}**",
            f"**{fmt_pct(ov['within_5_pct'] * 100)}**",
            f"**{fmt_pct(ov['coverage_90'] * 100)}**",
        ]
    )
    return pad_table(rows)


