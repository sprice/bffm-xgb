def _gen_per_domain_k20_from_notes_inputs(notes_inputs: dict) -> str:
    csv_rows = _notes_input_rows(notes_inputs, "baseline_comparison_per_domain_rows")

    def build_table(method_name: str, label: str) -> str:
        heading = f"**{label}:**\n\n"
        rows = [["Domain", "r", "Items", "CI Lower", "CI Upper"]]
        for csv_row in csv_rows:
            if int(csv_row["n_items"]) != 20 or csv_row["method"] != method_name:
                continue
            for d in DOMAIN_ORDER:
                dk = DOMAIN_KEYS_BASELINE[d]
                r_val = float(csv_row[f"r_{dk}"])
                items = int(float(csv_row[f"items_{dk}"]))
                ci_lo_key = f"r_{dk}_ci_lower"
                ci_hi_key = f"r_{dk}_ci_upper"
                ci_lo = (
                    fmt_r(float(csv_row[ci_lo_key]), 4)
                    if csv_row.get(ci_lo_key)
                    else "---"
                )
                ci_hi = (
                    fmt_r(float(csv_row[ci_hi_key]), 4)
                    if csv_row.get(ci_hi_key)
                    else "---"
                )
                rows.append(
                    [DOMAIN_LABELS[d], fmt_r(r_val, 4), str(items), ci_lo, ci_hi]
                )
            break
        return heading + pad_table(rows)

    parts = [
        build_table("domain_balanced", "Domain-Balanced (4 items per domain)"),
        build_table("mini_ipip", "Mini-IPIP (4 items per domain)"),
        build_table("first_n", "First-N (4 items per domain)"),
    ]
    return "\n\n".join(parts)


