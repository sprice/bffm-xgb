def load_json(path: Path) -> dict:
    with open(path) as f:
        return json.load(f)


def load_yaml(path: Path) -> dict:
    with open(path) as f:
        return yaml.safe_load(f)


def load_research_summary() -> dict:
    """Load cross-variant summary generated by scripts/build_research_summary.py."""
    if not RESEARCH_SUMMARY_PATH.exists():
        raise FileNotFoundError(
            f"Missing {RESEARCH_SUMMARY_PATH}. Run `make research-summary` first."
        )
    payload = load_json(RESEARCH_SUMMARY_PATH)
    variants = payload.get("variants")
    if not isinstance(variants, dict):
        raise ValueError(
            f"{RESEARCH_SUMMARY_PATH} is missing required 'variants' object."
        )
    return payload


def load_reference_notes_inputs() -> dict:
    """Load canonical reference inputs from research_summary.json."""
    payload = load_research_summary()
    notes_inputs = payload.get("reference_notes_inputs")
    if not isinstance(notes_inputs, dict):
        raise ValueError(
            f"{RESEARCH_SUMMARY_PATH} is missing required 'reference_notes_inputs' object."
        )
    return notes_inputs


def load_variant_notes_inputs(variant: str) -> dict:
    """Load variant-scoped NOTES inputs from research_summary.json."""
    payload = load_research_summary()
    variants = payload.get("variants", {})
    if not isinstance(variants, dict):
        raise ValueError(f"{RESEARCH_SUMMARY_PATH} has invalid 'variants' object.")
    variant_payload = variants.get(variant)
    if not isinstance(variant_payload, dict):
        raise KeyError(f"research_summary variant '{variant}' missing.")
    notes_inputs = variant_payload.get("notes_inputs")
    if not isinstance(notes_inputs, dict):
        raise KeyError(
            f"research_summary variant '{variant}' is missing required 'notes_inputs'."
        )
    return notes_inputs


def _notes_input_dict(notes_inputs: dict, key: str) -> dict:
    value = notes_inputs.get(key)
    if isinstance(value, dict):
        err = value.get("__error__")
        if isinstance(err, str):
            raise ValueError(f"research_summary reference input '{key}' error: {err}")
        return value
    raise KeyError(f"research_summary reference input '{key}' missing or invalid.")


def _notes_input_rows(notes_inputs: dict, key: str) -> list[dict]:
    value = notes_inputs.get(key)
    if isinstance(value, list):
        return [row for row in value if isinstance(row, dict)]
    raise KeyError(f"research_summary reference input '{key}' missing or invalid.")


def _resolve_calibration_policy_fields(policy: dict) -> tuple[str, str, str]:
    """Normalize calibration policy keys across artifact schema revisions."""
    full_50 = policy.get("n_items_50_or_more")
    sparse = policy.get("n_items_below_50", policy.get("n_items_20"))
    fallback = policy.get(
        "fallback_without_sparse_calibration",
        policy.get("other_n_items"),
    )
    if not isinstance(full_50, str) or not isinstance(sparse, str) or not isinstance(fallback, str):
        raise KeyError(
            "baseline_comparison_results.json config.calibration_policy is missing required keys."
        )
    return full_50, sparse, fallback


def load_hyperparameters_for_notes() -> dict:
    """Load hyperparameters from canonical research summary notes inputs."""
    notes_inputs = load_reference_notes_inputs()
    tuned = _notes_input_dict(notes_inputs, "tuned_params")
    hp = tuned.get("hyperparameters")
    if isinstance(hp, dict) and hp:
        return hp
    raise KeyError("research_summary reference input 'tuned_params.hyperparameters' missing.")


def fmt_r(val: float, places: int = 4) -> str:
    """Format a correlation value with fixed decimal places."""
    return f"{val:.{places}f}"


def fmt_r3(val: float) -> str:
    return fmt_r(val, 3)


def fmt_pct(val: float) -> str:
    return f"{val:.1f}%"


def fmt_f(val: float, places: int = 2) -> str:
    return f"{val:.{places}f}"


def fmt_opt_float(val: Any, places: int = 4) -> str:
    if isinstance(val, (int, float)):
        return fmt_f(float(val), places)
    return "---"


def _get_nested(d: dict, keys: list[str]) -> Any:
    curr: Any = d
    for key in keys:
        if not isinstance(curr, dict):
            return None
        curr = curr.get(key)
    return curr


def pad_table(rows: list[list[str]]) -> str:
    """Render a list-of-lists as a well-spaced markdown table."""
    n_cols = len(rows[0])
    widths = [max(len(str(row[i])) for row in rows) for i in range(n_cols)]
    lines = []
    for idx, row in enumerate(rows):
        cells = [" " + str(row[i]).ljust(widths[i]) + " " for i in range(n_cols)]
        lines.append("|" + "|".join(cells) + "|")
        if idx == 0:
            sep = ["-" * (widths[i] + 2) for i in range(n_cols)]
            lines.append("|" + "|".join(sep) + "|")
    return "\n".join(lines)


