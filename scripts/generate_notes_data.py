def _gen_domain_starvation_from_notes_inputs(notes_inputs: dict) -> str:
    csv_rows = _notes_input_rows(notes_inputs, "baseline_comparison_per_domain_rows")
    rows = [["Domain", "Items", "Share", "r", "CI Lower", "CI Upper"]]
    for csv_row in csv_rows:
        if int(csv_row["n_items"]) != 20 or csv_row["method"] != "adaptive_topk":
            continue
        # Sort domains by item count descending for starvation visualization
        domain_data = []
        for d in DOMAIN_ORDER:
            dk = DOMAIN_KEYS_BASELINE[d]
            items = int(float(csv_row[f"items_{dk}"]))
            r_val = float(csv_row[f"r_{dk}"])
            ci_lo = (
                float(csv_row[f"r_{dk}_ci_lower"])
                if csv_row.get(f"r_{dk}_ci_lower")
                else None
            )
            ci_hi = (
                float(csv_row[f"r_{dk}_ci_upper"])
                if csv_row.get(f"r_{dk}_ci_upper")
                else None
            )
            domain_data.append((d, items, r_val, ci_lo, ci_hi))
        domain_data.sort(key=lambda x: -x[1])
        for d, items, r_val, ci_lo, ci_hi in domain_data:
            share = f"{items * 100 // 20}%"
            rows.append(
                [
                    DOMAIN_LABELS[d],
                    str(items),
                    share,
                    fmt_r(r_val, 4),
                    fmt_r(ci_lo, 4) if ci_lo else "---",
                    fmt_r(ci_hi, 4) if ci_hi else "---",
                ]
            )
        break
    return pad_table(rows)


